CSDS	XDATA 0FF30h


	ORG 0000h
_RESET:
	LJMP _INIT

	
	ORG 0100h
_INIT:

	

_LOOP:
	CALL _KLAWIATURA
	LJMP _LOOP

_KLAWIATURA:
	SETB P1.6
	;enter
	MOV DPTR, #CSDS
	MOV A,#00000001b
	MOVX @DPTR,A
	JB P3.5,_ENTER

	;up
	MOV DPTR, #CSDS
	MOV A,#00001000b
	MOVX @DPTR,A
	JB P3.5,_UP

	;down
	MOV DPTR, #CSDS
	MOV A,#00010000b
	MOVX @DPTR,A
	JB P3.5,_DOWN

_KLAWIATURA_END:
	
	RET
_ENTER:
	SETB P1.7
	JMP _KLAWIATURA_END
_UP:
	SETB P1.5
	JMP _KLAWIATURA_END
_DOWN:
	CLR P1.5
	CLR P1.7
	JMP _KLAWIATURA_END

END