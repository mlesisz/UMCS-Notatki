
ORG	0000h
_RESET:
LJMP _INIT

	
ORG 0100h
_INIT:

	;MO  M1  M2  REN  TB8  RB8  TI  RI
	MOV SCON,#01010000b;M0=0,  M1=1,  M2=0,  REN=1,  TB8=0,  RB8=0,  TI=0,  RI=0	

	ANL TMOD,#00101111b
	ORL TMOD,#00100000b

	MOV TL1,#0FDh
	MOV TH1,#0FDh
	ANL PCON,#01111111b
	CLR TF1
	SETB TR1
	
_LOOP:
	JNB TI,_LOOP10
	MOV A,SBUF
	CLR TI

_LOOP10:
	JNB RI,_LOOP
	CLR RI
	MOV SBUF,A
	LJMP _LOOP

_MESSAGE:
DB	'WITAJ',0
END